  }

  Widget _buildPromotionsList(Color primaryGreen, Color goldColor) {
    List<PromotionModel> filtered = List.from(_editedPromotions); // Use local state

    // Apply Status Filter
    if (_statusFilter == 'Ativos') {
      filtered = filtered.where((p) => p.ativo).toList();
    } else if (_statusFilter == 'Inativos') {
      filtered = filtered.where((p) => !p.ativo).toList();
    }

    // Apply Order Filter
    if (_orderFilter == 'Título') {
      filtered.sort((a, b) => a.titulo.compareTo(b.titulo));
    } else if (_orderFilter == 'Ordem') {
      filtered.sort((a, b) => a.ordem.compareTo(b.ordem));
    }

    return ListView(
      padding: const EdgeInsets.all(24),
      children: [
        ...filtered.asMap().entries.map((entry) {
          // Find the original index in _editedPromotions to ensure we update the right one
          final realIndex = _editedPromotions.indexOf(entry.value);
          return _buildPromotionEditorCard(realIndex, entry.value, primaryGreen, goldColor);
        }),
        const SizedBox(height: 12),
        Center(
          child: TextButton.icon(
            onPressed: _createNewCard,
            icon: Icon(Icons.add_circle_outline, color: goldColor),
            label: Text(
              'Criar novo card',
              style: TextStyle(color: goldColor,
                fontWeight: FontWeight.bold,
                letterSpacing: 1.2,
              ),
            ),
            style: TextButton.styleFrom(
              padding: const EdgeInsets.symmetric(horizontal: 24, vertical: 16),
            ),
          ),
        ),
        const SizedBox(height: 40),
      ],
    );
  }

