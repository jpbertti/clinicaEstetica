  Widget _buildServiceDropdown(int index, PromotionModel promo, Color primaryGreen, Color goldColor) {
    String currentLinkedName = 'Nenhum vínculo';
    if (promo.servicoId != null) {
      final s = _services.where((s) => s.id == promo.servicoId).firstOrNull;
      currentLinkedName = s?.nome ?? 'Serviço não encontrado';
    } else if (promo.pacoteId != null) {
      final p = _packages.where((p) => p.id == promo.pacoteId).firstOrNull;
      currentLinkedName = p?.titulo != null ? 'PACOTE: ${p!.titulo}' : 'Pacote não encontrado';
    }

    return Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        Text(
          'Vincular a um Procedimento ou Pacote',
          style: TextStyle(fontSize: 10,
            fontWeight: FontWeight.bold,
            color: const Color(0xFF6E8F7B),
            letterSpacing: 1.2,
          ),
        ),
        const SizedBox(height: 8),
        InkWell(
          onTap: () => _showLinkingModal(index, promo, primaryGreen, goldColor),
          child: Container(
            padding: const EdgeInsets.symmetric(horizontal: 16, vertical: 14),
            decoration: BoxDecoration(
              color: Colors.white,
              borderRadius: BorderRadius.circular(12),
              border: Border.all(color: Colors.black.withOpacity(0.08)),
              boxShadow: [
                BoxShadow(
                  color: Colors.black.withOpacity(0.02),
                  blurRadius: 10,
                  offset: const Offset(0, 4),
                ),
              ],
            ),
            child: Row(
              children: [
                Icon(
                  promo.pacoteId != null ? Icons.inventory_2_outlined : Icons.spa_outlined,
                  color: goldColor,
                  size: 20,
                ),
                const SizedBox(width: 12),
                Expanded(
                  child: Text(
                    currentLinkedName,
                    style: TextStyle(fontSize: 14,
                      fontWeight: FontWeight.w500,
                      color: (promo.servicoId == null && promo.pacoteId == null)
                          ? Colors.black38 
                          : const Color(0xFF1A1A1A),
                    ),
                  ),
                ),
                Icon(Icons.keyboard_arrow_down, color: goldColor.withOpacity(0.5)),
              ],
            ),
          ),
        ),
      ],
    );
  }

