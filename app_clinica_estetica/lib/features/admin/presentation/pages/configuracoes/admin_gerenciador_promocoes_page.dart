                      onChanged: (val) {
                        setModalState(() {
                          searchQuery = val;
                        });
                      },
                      decoration: InputDecoration(
                        hintText: 'Buscar procedimentos ou pacotes...',
                        prefixIcon: const Icon(Icons.search, size: 20),
                        filled: true,
                        fillColor: Colors.white,
                        border: OutlineInputBorder(
                          borderRadius: BorderRadius.circular(15),
                          borderSide: BorderSide(color: Colors.black.withOpacity(0.05)),
                        ),
                        contentPadding: const EdgeInsets.symmetric(vertical: 0),
                      ),
                    ),
                  ),
                  Expanded(
                    child: ListView(
                      padding: const EdgeInsets.symmetric(horizontal: 24),
                      children: [
                        const SizedBox(height: 12),
                        _buildModalItem(
                          icon: Icons.link_off,
                          title: 'Remover vínculo atual',
                          onTap: () {
                            _updateLocalPromotionByIndex(index, promo.copyWith(clearServico: true, clearPacote: true));
                            Navigator.pop(context);
                          },
                          color: Colors.red.withOpacity(0.7),
                        ),
                        const SizedBox(height: 24),
                        
                        _buildModalSectionHeader('Procedimentos', goldColor),
                        if (filteredServices.isEmpty)
                          Padding(
                            padding: const EdgeInsets.symmetric(vertical: 20),
                            child: Text(
                              'Nenhum procedimento encontrado',
                              textAlign: TextAlign.center,
                              style: TextStyle(color: Colors.black38, fontSize: 13),
                            ),
                          ),
                        ...filteredServices.map((s) => _buildModalItem(
                          icon: Icons.spa_outlined,
                          title: s.nome,
                          isSelected: promo.servicoId == s.id,
                          goldColor: goldColor,
                          onTap: () {
                            _updateLocalPromotionByIndex(index, promo.copyWith(servicoId: s.id, clearPacote: true));
                            Navigator.pop(context);
                          },
                        )),
                        
                        const SizedBox(height: 32),
                        _buildModalSectionHeader('Pacotes', goldColor),
                        if (filteredPackages.isEmpty)
                          Padding(
                            padding: const EdgeInsets.symmetric(vertical: 20),
                            child: Text(
                              'Nenhum pacote encontrado',
                              textAlign: TextAlign.center,
                              style: TextStyle(color: Colors.black38, fontSize: 13),
                            ),
                          ),
                        ...filteredPackages.map((p) => _buildModalItem(
                          icon: Icons.inventory_2_outlined,
                          title: p.titulo,
                          isSelected: promo.pacoteId == p.id,
                          goldColor: goldColor,
                          onTap: () {
                            _updateLocalPromotionByIndex(index, promo.copyWith(pacoteId: p.id, clearServico: true));
                            Navigator.pop(context);
                          },
                        )),
                        const SizedBox(height: 50),
                      ],
                    ),
                  ),
                ],
              ),
            );
          },
        );
      },
    );
  }

  Widget _buildModalSectionHeader(String title, Color goldColor) {
    return Padding(
      padding: const EdgeInsets.only(bottom: 12, left: 4),
      child: Row(
        children: [
          Container(
            padding: const EdgeInsets.symmetric(horizontal: 10, vertical: 4),
            decoration: BoxDecoration(
              color: goldColor.withOpacity(0.1),
              borderRadius: BorderRadius.circular(6),
            ),
            child: Text(
              title,
              style: TextStyle(fontSize: 11,
                fontWeight: FontWeight.bold,
                color: goldColor,
                letterSpacing: 1.2,
              ),
            ),
          ),
          const SizedBox(width: 8),
          Expanded(child: Divider(color: goldColor.withOpacity(0.1))),
        ],
      ),
    );
  }

  Widget _buildModalItem({
    required IconData icon,
    required String title,
    required VoidCallback onTap,
    bool isSelected = false,
    Color? color,
    Color? goldColor,
  }) {
    return Container(
      margin: const EdgeInsets.only(bottom: 8),
      child: ListTile(
        onTap: onTap,
        dense: true,
        shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(12)),
        tileColor: isSelected ? (goldColor ?? const Color(0xFFC7A36B)).withOpacity(0.05) : null,
        leading: Icon(icon, color: color ?? (isSelected ? goldColor : Colors.black45), size: 18),
        title: Text(
          title,
          style: TextStyle(fontSize: 14,
            fontWeight: isSelected ? FontWeight.bold : FontWeight.w500,
            color: isSelected ? (goldColor ?? const Color(0xFFC7A36B)) : Colors.black87,
          ),
        ),
        trailing: isSelected ? Icon(Icons.check_circle, color: goldColor, size: 20) : null,
      ),
    );
  }

