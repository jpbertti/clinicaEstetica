class AdminGerenciadorPromocoesPage extends StatefulWidget {
  const AdminGerenciadorPromocoesPage({super.key});

  @override
  State<AdminGerenciadorPromocoesPage> createState() => _AdminGerenciadorPromocoesPageState();
}

class _AdminGerenciadorPromocoesPageState extends State<AdminGerenciadorPromocoesPage> {
  final _promotionRepo = SupabasePromotionRepository();
  final _serviceRepo = SupabaseServiceRepository();
  final _packageRepo = SupabasePackageRepository(Supabase.instance.client);
  final _dashboardRepo = SupabaseDashboardRepository();
  final _picker = ImagePicker();
  
  bool _isLoading = true;
  bool _isUploading = false;
  bool _hasChanges = false;
  List<PromotionModel> _promotions = [];
  List<PromotionModel> _editedPromotions = [];
  final Set<String> _pendingDeletions = {};
  List<ServiceModel> _services = [];
  List<PacoteTemplateModel> _packages = [];

  @override
  void initState() {
    super.initState();
    _loadData();
  }

  Future<void> _loadData() async {
    setState(() => _isLoading = true);
    try {
      final results = await Future.wait<dynamic>([
        _promotionRepo.getAllPromotionsAdmin(),
        _serviceRepo.getActiveServices(),
        _packageRepo.getTemplates(),
      ]);

      setState(() {
        _promotions = results[0] as List<PromotionModel>;
        _editedPromotions = List.from(_promotions);
        _pendingDeletions.clear();
        _hasChanges = false;
        _services = results[1] as List<ServiceModel>;
        _packages = results[2] as List<PacoteTemplateModel>;
        _isLoading = false;
      });
    } catch (e) {
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(content: Text('Erro ao carregar dados: $e'), backgroundColor: Colors.red),
        );
      }
      setState(() => _isLoading = false);
    }
  }

  void _createNewCard() {
    setState(() {
      _editedPromotions.add(PromotionModel(
        id: '', // Will be generated by UUID on insert
        titulo: 'Nova Promoção',
        subtitulo: 'Descrição curta aqui',
        imagemUrl: 'https://images.unsplash.com/photo-1570172619644-dfd03ed5d881?q=80&w=2070&auto=format&fit=crop',
        ordem: _editedPromotions.length + 1,
        ativo: true,
      ));
      _hasChanges = true;
    });
  }

  void _updateLocalPromotionByIndex(int index, PromotionModel updated) {
    setState(() {
      _editedPromotions[index] = updated;
      _hasChanges = true;
    });
  }

  Future<void> _saveAllChanges() async {
    setState(() => _isLoading = true);
    final adminId = Supabase.instance.client.auth.currentUser?.id;
    
    try {
      // 1. Deletions
      for (final id in _pendingDeletions) {
        // Encontrar o título original para o log
        final originalPromo = _promotions.where((p) => p.id == id).firstOrNull;
        final title = originalPromo?.titulo ?? 'Promoção Desconhecida';
        
        await _promotionRepo.deletePromotion(id);
        
        await _dashboardRepo.logActivity(
          tipo: 'configuracao',
          titulo: 'Promoção Excluída',
          descricao: 'A promoção "$title" foi excluída.',
          userId: adminId,
          metadata: {
            'changes': [
              {'campo': 'Promoção', 'antigo': title, 'novo': 'Excluída'}
            ]
          },
        );
      }

      // 2. Inserts & Updates
      for (final promo in _editedPromotions) {
        if (promo.id.isEmpty) {
          await _promotionRepo.insertPromotion(promo);
          await _dashboardRepo.logActivity(
            tipo: 'configuracao',
            titulo: 'Promoção Criada',
            descricao: 'A promoção "${promo.titulo}" foi criada.',
            userId: adminId,
            metadata: {
              'changes': [
                {'campo': 'Nova Promoção', 'antigo': 'Não existia', 'novo': promo.titulo}
              ]
            },
          );
        } else {
          // Verificar se houve mudança real antes de logar (opcional, mas bom)
          final original = _promotions.where((p) => p.id == promo.id).firstOrNull;
          if (original != null && (original.titulo != promo.titulo || original.ativo != promo.ativo || original.servicoId != promo.servicoId || original.pacoteId != promo.pacoteId)) {
            final changes = <Map<String, String>>[];
            if (original.titulo != promo.titulo) {
              changes.add({'campo': 'Título (${original.titulo})', 'antigo': original.titulo, 'novo': promo.titulo});
            }
            if (original.subtitulo != promo.subtitulo) {
              changes.add({'campo': 'Subtítulo (${promo.titulo})', 'antigo': original.subtitulo, 'novo': promo.subtitulo});
            }
            if (original.ativo != promo.ativo) {
              changes.add({'campo': 'Status (${promo.titulo})', 'antigo': original.ativo ? 'Ativo' : 'Inativo', 'novo': promo.ativo ? 'Ativo' : 'Inativo'});
            }
            if (original.servicoId != promo.servicoId || original.pacoteId != promo.pacoteId) {
               String oldLinked = 'Nenhum';
               if (original.servicoId != null) oldLinked = 'Serviço ID: ${original.servicoId}';
               if (original.pacoteId != null) oldLinked = 'Pacote ID: ${original.pacoteId}';
               
               String newLinked = 'Nenhum';
               if (promo.servicoId != null) newLinked = 'Serviço ID: ${promo.servicoId}';
               if (promo.pacoteId != null) newLinked = 'Pacote ID: ${promo.pacoteId}';
               
               changes.add({'campo': 'Vínculo (${promo.titulo})', 'antigo': oldLinked, 'novo': newLinked});
            }

            await _promotionRepo.updatePromotion(promo);
            await _dashboardRepo.logActivity(
              tipo: 'configuracao',
              titulo: 'Promoção Editada',
              descricao: 'A promoção "${promo.titulo}" foi alterada.',
              userId: adminId,
              metadata: {'changes': changes},
            );
          } else if (original == null) {
             // Caso improvável mas possível se o estado dessincronizar
             await _promotionRepo.updatePromotion(promo);
          }
        }
      }

      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          const SnackBar(content: Text('Todas as alterações foram salvas!'), backgroundColor: Color(0xFF2D5A46)),
        );
      }
      await _loadData();
    } catch (e) {
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(content: Text('Erro ao salvar tudo: $e'), backgroundColor: Colors.red),
        );
      }
      setState(() => _isLoading = false);
    }
  }

  void _discardChanges() {
    setState(() {
      _editedPromotions = List.from(_promotions);
      _pendingDeletions.clear();
      _hasChanges = false;
    });
  }

  Future<void> _pickAndUploadImage(int index, PromotionModel promo) async {
    try {
      final XFile? image = await _picker.pickImage(
        source: ImageSource.gallery,
        imageQuality: 70,
        maxWidth: 1200,
      );

      if (image == null) return;

      setState(() => _isUploading = true);

      final bytes = await image.readAsBytes();
      final url = await _promotionRepo.uploadPromotionImage(image.name, bytes);

      if (url != null) {
        // Update local state instead of saving to repo
        _updateLocalPromotionByIndex(index, promo.copyWith(imagemUrl: url));
        
        // Note: Image deletion from storage still happens immediately as it's a file operation
        if (promo.imagemUrl.isNotEmpty && promo.imagemUrl.contains('supabase')) {
          await _promotionRepo.deletePromotionImage(promo.imagemUrl);
        }
      }
    } catch (e) {
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(content: Text('Erro no upload: $e'), backgroundColor: Colors.red),
        );
      }
    } finally {
      setState(() => _isUploading = false);
    }
  }

  @override
  Widget build(BuildContext context) {
    const goldColor = Color(0xFFC7A36B);

    return Scaffold(
      backgroundColor: AppColors.background,
      body: _isLoading 
        ? const Center(child: CircularProgressIndicator(color: goldColor))
        : Column(
            children: [
              _buildFilters(AppColors.primary, goldColor),
              Expanded(
                child: RefreshIndicator(
                  onRefresh: _loadData,
                  color: goldColor,
                  child: _buildPromotionsList(AppColors.primary, goldColor),
                ),
              ),
              if (_hasChanges) _buildBottomBar(goldColor),
            ],
          ),
    );
  }

  String _statusFilter = 'Todos';
  String _orderFilter = 'Nenhum';

  Widget _buildFilters(Color primaryGreen, Color goldColor) {
    return Container(
      padding: const EdgeInsets.symmetric(horizontal: 24, vertical: 16),
      color: AppColors.background,
      child: Row(
        children: [
          Expanded(
            child: _buildFilterDropdown(
              label: 'Status',
              value: _statusFilter,
              items: ['Todos', 'Ativos', 'Inativos'],
              onChanged: (val) => setState(() => _statusFilter = val!),
              goldColor: goldColor,
            ),
          ),
          const SizedBox(width: 16),
          Expanded(
            child: _buildFilterDropdown(
              label: 'Ordenar',
              value: _orderFilter,
              items: ['Nenhum', 'Título', 'Ordem'],
              onChanged: (val) => setState(() => _orderFilter = val!),
              goldColor: goldColor,
            ),
          ),
        ],
      ),
    );
  }

  Widget _buildFilterDropdown({
    required String label,
    required String value,
    required List<String> items,
    required ValueChanged<String?> onChanged,
    required Color goldColor,
  }) {
    return Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        Text(
          label,
          style: TextStyle(fontSize: 10,
            fontWeight: FontWeight.bold,
            color: goldColor,
            letterSpacing: 1.2,
          ),
        ),
        const SizedBox(height: 4),
        Container(
          height: 40,
          padding: const EdgeInsets.symmetric(horizontal: 12),
          decoration: BoxDecoration(
            color: const Color(0xFFF8FBF9),
            borderRadius: BorderRadius.circular(10),
            border: Border.all(color: goldColor.withOpacity(0.2)),
          ),
          child: DropdownButtonHideUnderline(
            child: DropdownButton<String>(
              value: value,
              isExpanded: true,
              icon: Icon(Icons.keyboard_arrow_down, color: goldColor, size: 18),
              style: TextStyle(color: const Color(0xFF2D5A46),
                fontSize: 13,
                fontWeight: FontWeight.w600,
              ),
